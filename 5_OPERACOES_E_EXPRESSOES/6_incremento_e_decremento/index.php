<?php

    function incremento($num){
        $num++;
        echo "valor: " .$num . "<br>";
        $num = $num+1;
        echo "valor: " .$num . "<br>";
    }

    function decremento($num){
        $num--;
        echo "valor: " .$num . "<br>";
        $num = $num-1;
        echo "valor: " .$num . "<br>";
    }

    incremento(11);
    incremento(20);
    decremento(10);
    decremento(5);









    



